// Ques 3: How much space does following data types take ?

// 1.int
// 2.bool
// 3.float
#include <iostream>
using namespace std;
int main() {
    cout << "Size of int: " << sizeof(int) << " bytes" << endl; // Output size of int
    cout << "Size of bool: " << sizeof(bool) << " bytes" << endl; // Output size of bool
    cout << "Size of float: " << sizeof(float) << " bytes" << endl; // Output size of float
    return 0; // Indicate successful completion
}
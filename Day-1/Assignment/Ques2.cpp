// Ques 2: Print 10 using 2 positive numbers less than 6  in C++ as output.
#include <iostream>
using namespace std;
int main() {
    int num1 = 4; // First positive number less than 6
    int num2 = 6; // Second positive number less than 6

    // Calculate the sum of the two numbers
    int result = num1 + num2;

    // Output the result
    cout << "The sum of " << num1 << " and " << num2 << " is: " << result << endl;

    return 0; // Indicate successful completion
}